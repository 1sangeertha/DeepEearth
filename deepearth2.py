import torch
import torch.nn.functional as F
import numpy as np
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches

class GradCAM:
    def __init__(self, model, target_layer):
        self.model       = model
        self.gradients   = None
        self.activations = None
        target_layer.register_forward_hook(self._save_activation)
        target_layer.register_full_backward_hook(self._save_gradient)

    def _save_activation(self, module, input, output):
        self.activations = output.detach()

    def _save_gradient(self, module, grad_input, grad_output):
        self.gradients = grad_output[0].detach()

    def generate(self, x):
        self.model.eval()
        x = x.clone().requires_grad_(True)

        output = self.model(x)  # (1, 11, 32, 32)

        # ✅ FIX: get dominant class across ALL pixels (not center pixel)
        pred_flat  = output.argmax(dim=1).flatten()         # (32*32,)
        class_idx  = int(torch.bincount(pred_flat).argmax()) # single int

        # Score = mean logit of dominant class across all pixels
        score = output[0, class_idx].mean()  # scalar ✅

        self.model.zero_grad()
        score.backward(retain_graph=True)

        # Grad-CAM: weight activations by gradient importance
        weights = self.gradients.mean(dim=(2, 3), keepdim=True)
        cam     = (weights * self.activations).sum(dim=1, keepdim=True)
        cam     = F.relu(cam)
        cam     = F.interpolate(cam, size=(x.shape[2], x.shape[3]),
                                mode='bilinear', align_corners=False)
        cam     = cam.squeeze().cpu().numpy()

        if cam.max() > cam.min():
            cam = (cam - cam.min()) / (cam.max() - cam.min())

        return cam, class_idx


