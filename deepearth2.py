# ConvLSTM Cell + Temporal U-Net
# Processes 4 timesteps of satellite data per pixel
# ============================================================

class ConvLSTMCell(nn.Module):
    def __init__(self, in_channels, hidden_channels, kernel_size=3):
        super().__init__()
        pad = kernel_size // 2
        self.hidden_channels = hidden_channels

        # All 4 gates in one conv (input, forget, cell, output)
        self.gates = nn.Conv2d(
            in_channels + hidden_channels,
            4 * hidden_channels,
            kernel_size, padding=pad
        )

    def forward(self, x, h, c):
        # x: B x C x H x W
        # h, c: B x hidden x H x W
        combined = torch.cat([x, h], dim=1)
        gates    = self.gates(combined)

        i, f, g, o = gates.chunk(4, dim=1)
        i = torch.sigmoid(i)   # input gate
        f = torch.sigmoid(f)   # forget gate
        g = torch.tanh(g)      # cell gate
        o = torch.sigmoid(o)   # output gate

        c_new = f * c + i * g
        h_new = o * torch.tanh(c_new)
        return h_new, c_new

    def init_hidden(self, batch_size, h, w, device):
        return (
            torch.zeros(batch_size, self.hidden_channels, h, w, device=device),
            torch.zeros(batch_size, self.hidden_channels, h, w, device=device)
        )


class TemporalUNet(nn.Module):
    """
    ConvLSTM encoder → U-Net decoder
    Input:  B x T x C x H x W   (T=4 timesteps, C=6 bands)
    Output: B x num_classes x H x W
    """
    def __init__(self, in_channels=6, hidden=64, num_classes=11, timesteps=4):
        super().__init__()
        self.timesteps = timesteps

        # ConvLSTM encoder — processes each timestep
        self.convlstm = ConvLSTMCell(in_channels, hidden)

        # U-Net decoder on final hidden state
        self.enc1 = DoubleConv(hidden, 64)
        self.enc2 = DoubleConv(64, 128)
        self.pool = nn.MaxPool2d(2)
        self.dropout = nn.Dropout2d(0.3)

        self.bottleneck = DoubleConv(128, 256)

        self.up2  = nn.ConvTranspose2d(256, 128, 2, stride=2)
        self.dec2 = DoubleConv(256, 128)
        self.up1  = nn.ConvTranspose2d(128, 64, 2, stride=2)
        self.dec1 = DoubleConv(128, 64)

        self.out_conv = nn.Conv2d(64, num_classes, kernel_size=1)

    def forward(self, x):
        # x: B x T x C x H x W
        B, T, C, H, W = x.shape

        # Initialize hidden state
        h, c = self.convlstm.init_hidden(B, H, W, x.device)

        # Process each timestep through ConvLSTM
        for t in range(T):
            h, c = self.convlstm(x[:, t], h, c)

        # h is now the temporal summary: B x hidden x H x W
        # Pass through U-Net decoder
        e1 = self.enc1(h)
        e2 = self.enc2(self.dropout(self.pool(e1)))
        b  = self.bottleneck(self.pool(e2))

        d2 = self.dec2(torch.cat([self.up2(b),  e2], dim=1))
        d1 = self.dec1(torch.cat([self.up1(d2), e1], dim=1))

        return self.out_conv(d1)


