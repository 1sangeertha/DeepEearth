            for j in range(0, W - patch_size, stride):
                patch = features[i:i+patch_size, j:j+patch_size]
                if patch.shape[:2] != (patch_size, patch_size):
                    continue

                x = torch.tensor(patch).permute(2, 0, 1) \
                         .unsqueeze(0).float().to(device)

                logits   = model(x)                      # 1×11×32×32
                probs    = F.softmax(logits, dim=1)      # 1×11×32×32
                max_conf, pred = probs.max(dim=1)        # 1×32×32

                pred     = pred[0].cpu().numpy()         # 32×32
                max_conf = max_conf[0].cpu().numpy()     # 32×32

