def create_patches(X, y, size=16):
    X_patches = []
    y_patches = []

    h, w, _ = X.shape

    for i in range(0, h - size, size):
        for j in range(0, w - size, size):
            patch = X[i:i+size, j:j+size]
            label_patch = y[i:i+size, j:j+size]

            unique, counts = np.unique(label_patch, return_counts=True)
            majority = unique[np.argmax(counts)]

            X_patches.append(patch)
            y_patches.append(majority)

    return np.array(X_patches), np.array(y_patches)

