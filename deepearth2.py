from google_auth_oauthlib.flow import InstalledAppFlow
from google.auth.transport.requests import Request
from googleapiclient.discovery import build
import pickle, os

SCOPES     = ['https://www.googleapis.com/auth/gmail.send']
token_path = f'{SAVE_DIR}/gmail_token.pkl'

flow = InstalledAppFlow.from_client_secrets_file(
           f'{SAVE_DIR}/credentials.json', SCOPES)

# Generate auth URL manually
flow.redirect_uri = 'urn:ietf:wg:oauth:2.0:oob'
auth_url, _ = flow.authorization_url(prompt='consent')

print("=" * 60)
print("STEP 1 — Open this URL in your browser:")
print()
print(auth_url)
print()
print("STEP 2 — Sign in with mahitha.chippa05@gmail.com")
print("STEP 3 — Click Allow")
print("STEP 4 — Copy the code shown and paste below")
print("=" * 60)

code = input("Paste the authorization code here: ")

flow.fetch_token(code=code)
creds = flow.credentials

with open(token_path, 'wb') as f:
    pickle.dump(creds, f)

gmail_service = build('gmail', 'v1', credentials=creds)
print("✅ Gmail authenticated and ready!")