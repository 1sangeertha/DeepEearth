def build_feature_stack(region, region_name, scale=120):
    print(f"\n🛰️  Fetching {region_name}...")

    bands = ['NDVI', 'NDWI', 'NDBI', 'NBR', 'EVI', 'MNDWI']

    coords = region.bounds().getInfo()['coordinates'][0]
    xmin = min(c[0] for c in coords)
    ymin = min(c[1] for c in coords)
    xmax = max(c[0] for c in coords)
    ymax = max(c[1] for c in coords)

    tiles = split_region([xmin, ymin, xmax, ymax], n=2)

    tile_arrays = {f'{b}_19': [] for b in bands}
    tile_arrays.update({f'{b}_24': [] for b in bands})

    for ti, tile_coords in enumerate(tiles):
        tile_geom = ee.Geometry.Rectangle(tile_coords)
        img_2019 = get_multispectral_collection('2019-01-01', '2019-12-31', tile_geom)
        img_2024 = get_multispectral_collection('2024-01-01', '2024-12-31', tile_geom)
        img_2019 = compute_indices(img_2019).reproject(crs='EPSG:4326', scale=scale)
        img_2024 = compute_indices(img_2024).reproject(crs='EPSG:4326', scale=scale)
        stack = img_2019.select(bands).addBands(img_2024.select(bands))

        try:
            data = stack.sampleRectangle(region=tile_geom, defaultValue=0)
            for b in bands:
                tile_arrays[f'{b}_19'].append(np.array(data.get(b).getInfo()))
                tile_arrays[f'{b}_24'].append(np.array(data.get(f'{b}_1').getInfo()))
            print(f"   ✅ Tile {ti+1}/4 done")
        except Exception as e:
            print(f"   ⚠️  Tile {ti+1}/4 failed: {e}")
            return None, None

    # ✅ FIX: Crop all tiles to same size before merging
    def safe_merge(tiles_list):
        """Crop tiles to minimum shared size then hstack/vstack"""
        # tiles: [top-left, top-right, bottom-left, bottom-right]
        tl, tr, bl, br = tiles_list

        # Crop rows to match vertically (top pair and bottom pair)
        min_row_top = min(tl.shape[0], tr.shape[0])
        min_row_bot = min(bl.shape[0], br.shape[0])
        tl = tl[:min_row_top, :]; tr = tr[:min_row_top, :]
        bl = bl[:min_row_bot, :]; br = br[:min_row_bot, :]

        # Crop cols to match horizontally
        min_col_left  = min(tl.shape[1], bl.shape[1])
        min_col_right = min(tr.shape[1], br.shape[1])
        tl = tl[:, :min_col_left];  bl = bl[:, :min_col_left]
        tr = tr[:, :min_col_right]; br = br[:, :min_col_right]

        top = np.hstack([tl, tr])
        bot = np.hstack([bl, br])

        min_cols = min(top.shape[1], bot.shape[1])
        return np.vstack([top[:, :min_cols], bot[:, :min_cols]])

    arrays = {}
    for b in bands:
        arrays[f'{b}_19'] = safe_merge(tile_arrays[f'{b}_19'])
        arrays[f'{b}_24'] = safe_merge(tile_arrays[f'{b}_24'])

    X = np.stack(list(arrays.values()), axis=-1)
    print(f"   ✅ Full feature stack: {X.shape}")
    return X, arrays

