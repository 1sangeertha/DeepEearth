def get_yearly_stack(region, year_start, year_end, scale=120):
    """Get 6-channel index stack for a single year"""
    bands = ['NDVI', 'NDWI', 'NDBI', 'NBR', 'EVI', 'MNDWI']

    coords = region.bounds().getInfo()['coordinates'][0]
    xmin = min(c[0] for c in coords)
    ymin = min(c[1] for c in coords)
    xmax = max(c[0] for c in coords)
    ymax = max(c[1] for c in coords)
    tiles = split_region([xmin, ymin, xmax, ymax], n=2)

    tile_arrays = {b: [] for b in bands}

    for tile_coords in tiles:
        tile_geom = ee.Geometry.Rectangle(tile_coords)
        img = get_multispectral_collection(year_start, year_end, tile_geom)
        img = compute_indices(img).reproject(crs='EPSG:4326', scale=scale)

        try:
            data = img.select(bands).sampleRectangle(
                region=tile_geom, defaultValue=0
            )
            for b in bands:
                tile_arrays[b].append(np.array(data.get(b).getInfo()))
        except Exception as e:
            print(f"      ⚠️  Tile failed: {e}")
            return None

    # Safe merge tiles
    def safe_merge(tiles_list):
        tl, tr, bl, br = tiles_list
        min_row_top = min(tl.shape[0], tr.shape[0])
        min_row_bot = min(bl.shape[0], br.shape[0])
        tl, tr = tl[:min_row_top], tr[:min_row_top]
        bl, br = bl[:min_row_bot], br[:min_row_bot]
        min_col_l = min(tl.shape[1], bl.shape[1])
        min_col_r = min(tr.shape[1], br.shape[1])
        tl, bl = tl[:, :min_col_l], bl[:, :min_col_l]
        tr, br = tr[:, :min_col_r], br[:, :min_col_r]
        top = np.hstack([tl, tr])
        bot = np.hstack([bl, br])
        mc  = min(top.shape[1], bot.shape[1])
        return np.vstack([top[:, :mc], bot[:, :mc]])

    merged = {}
    for b in bands:
        merged[b] = safe_merge(tile_arrays[b])

    # Stack → H x W x 6
    X = np.stack([merged[b] for b in bands], axis=-1)
    return X


