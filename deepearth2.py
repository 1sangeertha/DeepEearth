    dw19   = dw_2019[:H, :W]
    dw24   = dw_2024[:H, :W]
    labels = make_labels(arr19[:H,:W,:], arr24[:H,:W,:])
    labels[(dw19 == 1) & (dw24 == 6)] = 4
    labels[(dw19 == 1) & (dw24 == 7)] = 2
    labels[(dw19 == 1) & (dw24 == 2)] = 2
    labels[(dw19 == 1) & (dw24 == 5)] = 3
    labels[(dw19 == 5) & (dw24 == 7)] = 3
    labels[(dw19 == 0) & (dw24 == 7)] = 8
    labels[(dw19 == 0) & (dw24 == 2)] = 8
    labels[(dw19 == 7) & (dw24 == 6)] = 5
    labels[(dw19 == 5) & (dw24 == 6)] = 6
    labels[(dw19 != 4) & (dw24 == 4)] = 10
    labels[(dw19 == 3) & (dw24 != 3)] = 1
    labels[(dw19 == dw24)]             = 0
    return labels

