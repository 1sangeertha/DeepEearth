def get_region_prediction(region_name, model):
    """Re-run ConvLSTM prediction for a region, return pred_map"""
    temporal_stack = TEMPORAL_DATA[region_name]
    T, H, W, C = temporal_stack.shape
    H_even = (H // 2) * 2
    W_even = (W // 2) * 2
    temporal_stack = temporal_stack[:, :H_even, :W_even, :]

    pred_map  = np.zeros((H_even, W_even), dtype=np.float32)
    count_map = np.zeros((H_even, W_even), dtype=np.float32)

    model.eval()
    with torch.no_grad():
        for i in range(0, H_even - PATCH_SIZE, PATCH_SIZE // 2):
            for j in range(0, W_even - PATCH_SIZE, PATCH_SIZE // 2):
                patch = temporal_stack[:, i:i+PATCH_SIZE, j:j+PATCH_SIZE, :]
                if patch.shape[1:3] != (PATCH_SIZE, PATCH_SIZE): continue
                t    = torch.tensor(patch.transpose(0,3,1,2)) \
                           .unsqueeze(0).float().to(device)
                out  = model(t)
                pred = torch.argmax(out, dim=1).squeeze().cpu().numpy().astype(float)
                pred_map[i:i+PATCH_SIZE, j:j+PATCH_SIZE]  += pred
                count_map[i:i+PATCH_SIZE, j:j+PATCH_SIZE] += 1

    count_map = np.where(count_map == 0, 1, count_map)
    return (pred_map / count_map).astype(int)


