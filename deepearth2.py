def remap_impossible_classes_v2(name, pred_map):
    """
    v2: checks per-region override first, falls back to ecosystem type
    """
    if name in REGION_IMPOSSIBLE_OVERRIDES:
        impossible = REGION_IMPOSSIBLE_OVERRIDES[name]
    else:
        eco_type   = REGION_TYPES.get(name, 'urban')
        impossible = IMPOSSIBLE_CLASSES.get(eco_type, [])

    if not impossible:
        return pred_map.copy(), 0.0, []

    cleaned = pred_map.copy()
    for cls in impossible:
        cleaned[cleaned == cls] = 0

    n_sup = (pred_map != cleaned).sum()
    pct   = 100 * n_sup / pred_map.size
    return cleaned, pct, impossible


