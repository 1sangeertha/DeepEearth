def predict_static_calibrated(model, region_name, region_geom,
                               conf_threshold=0.75):
    """Static 2-year comparison with high confidence threshold"""
    arr19 = fetch_array(region_geom, *YEAR_2019)
    arr24 = fetch_array(region_geom, *YEAR_2024)

    H = min(arr19.shape[0], arr24.shape[0])
