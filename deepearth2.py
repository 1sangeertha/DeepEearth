def compute_alert_v5(pred_map):
    total  = pred_map.size
    counts = np.bincount(pred_map.flatten(), minlength=NUM_CLASSES)
    score  = 0
    for i in range(NUM_CLASSES):
        pct = 100 * counts[i] / total
        # Only count if pixel % exceeds minimum threshold
        if pct >= MIN_PIXEL_PCT[i]:
            raw    = ALERT_WEIGHTS_V3[i] * pct
            capped = min(raw, 20)
            score += capped
    return score, get_alert_level_v3(score), counts

