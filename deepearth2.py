    arr19 = arr19[:H, :W, :]
    arr24 = arr24[:H, :W, :]

    features  = np.concatenate([arr19, arr24], axis=-1)  # H×W×12
    pred_map  = np.zeros((H, W), dtype=np.int64)
    conf_map  = np.zeros((H, W), dtype=np.float32)
    count_map = np.zeros((H, W), dtype=np.int32)

    model.eval()
    with torch.no_grad():
        for i in range(0, H - PATCH_SIZE, STRIDE):
            for j in range(0, W - PATCH_SIZE, STRIDE):
                patch = features[i:i+PATCH_SIZE, j:j+PATCH_SIZE, :]
                if patch.shape[:2] != (PATCH_SIZE, PATCH_SIZE): continue

                x    = torch.tensor(
                           patch.transpose(2,0,1)[np.newaxis],
                           dtype=torch.float32).to(device)
                out  = torch.softmax(model(x), dim=1)
                conf, pred = out.max(dim=1)

                pred = pred.cpu().numpy()[0]
                conf = conf.cpu().numpy()[0]

                pred_map[i:i+PATCH_SIZE, j:j+PATCH_SIZE]  += pred
                conf_map[i:i+PATCH_SIZE, j:j+PATCH_SIZE]  += conf
                count_map[i:i+PATCH_SIZE, j:j+PATCH_SIZE] += 1

    count_map = np.maximum(count_map, 1)
    pred_map  = (pred_map / count_map).astype(np.int64)
    conf_map  = (conf_map / count_map).astype(np.float32)

    # High confidence threshold — only flag if very sure
    pred_map[conf_map < conf_threshold] = 0

    # Smooth
    smoothed = ndimage.uniform_filter(
        pred_map.astype(float), size=5).astype(np.int64)
    return smoothed

