def compute_indices(image):
    """Compute NDVI, NDWI, NDBI, EVI, NBR from multi-spectral image"""

    # NDVI — vegetation health
    ndvi = image.normalizedDifference(['B8', 'B4']).rename('NDVI')

    # NDWI — water bodies (Green - NIR)
    ndwi = image.normalizedDifference(['B3', 'B8']).rename('NDWI')

    # NDBI — built-up/urban (SWIR1 - NIR)
    ndbi = image.normalizedDifference(['B11', 'B8']).rename('NDBI')

    # NBR — burn ratio (NIR - SWIR2)
    nbr = image.normalizedDifference(['B8', 'B12']).rename('NBR')

    # EVI — enhanced vegetation (less soil noise than NDVI)
    evi = image.expression(
        '2.5 * ((NIR - RED) / (NIR + 6 * RED - 7.5 * BLUE + 1))',
        {'NIR': image.select('B8'),
         'RED': image.select('B4'),
         'BLUE': image.select('B2')}
    ).rename('EVI')

    # MNDWI — modified water (Green - SWIR1, detects sand mining well)
    mndwi = image.normalizedDifference(['B3', 'B11']).rename('MNDWI')

    return image.addBands([ndvi, ndwi, ndbi, nbr, evi, mndwi])

