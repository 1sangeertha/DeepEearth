def fetch_array(region_geom, date_start, date_end):
    img = (ee.ImageCollection('COPERNICUS/S2_SR_HARMONIZED')
           .filterBounds(region_geom)
           .filterDate(date_start, date_end)
           .filter(ee.Filter.lt('CLOUDY_PIXEL_PERCENTAGE', 20))
           .median()
           .reproject(crs='EPSG:4326', scale=SCALE))

    ndvi  = img.normalizedDifference(['B8','B4']).rename('NDVI')
    ndwi  = img.normalizedDifference(['B3','B8']).rename('NDWI')
    ndbi  = img.normalizedDifference(['B11','B8']).rename('NDBI')
    nbr   = img.normalizedDifference(['B8','B12']).rename('NBR')
    evi   = img.expression(
                '2.5*(NIR-RED)/(NIR+6*RED-7.5*BLUE+1)',
                {'NIR':img.select('B8'),'RED':img.select('B4'),
                 'BLUE':img.select('B2')}).rename('EVI')
    mndwi = img.normalizedDifference(['B3','B11']).rename('MNDWI')
    stack = ee.Image.cat([ndvi,ndwi,ndbi,nbr,evi,mndwi])
    d     = stack.sampleRectangle(region=region_geom, defaultValue=0).getInfo()
    bands = ['NDVI','NDWI','NDBI','NBR','EVI','MNDWI']
    return np.stack([np.array(d['properties'][b],
                    dtype=np.float32) for b in bands], axis=-1)

def make_labels(arr19, arr24):
    ndvi_19, ndwi_19 = arr19[:,:,0], arr19[:,:,1]
    ndvi_24, ndwi_24 = arr24[:,:,0], arr24[:,:,1]
    ndbi_24          = arr24[:,:,2]
    nbr_19, nbr_24   = arr19[:,:,3], arr24[:,:,3]
    delta_ndvi = ndvi_24 - ndvi_19
    delta_ndwi = ndwi_24 - ndwi_19
    delta_nbr  = nbr_24  - nbr_19
    labels = np.zeros(ndvi_24.shape, dtype=np.int64)
    labels[delta_ndvi < -0.05]                       = 1
    labels[delta_ndvi < -0.15]                       = 2
    labels[(delta_ndvi < -0.08) & (ndvi_19 > 0.3)]  = 3
    labels[(ndbi_24 > 0.1) & (delta_ndvi < -0.05)]  = 4
    labels[(ndbi_24 > 0.2) & (delta_ndvi < -0.1)]   = 5
    labels[(ndvi_24 < 0.1) & (delta_ndvi < -0.1)]   = 6
    labels[(ndwi_19 > 0.1) & (ndvi_24 < 0.15)]      = 7
    labels[(delta_ndwi < -0.1) & (ndwi_19 > 0.0)]   = 8
    labels[delta_nbr < -0.2]                         = 9
    labels[(delta_ndvi > 0.1) & (ndvi_24 > 0.2)]    = 10
    return labels

def extract_patches(features, labels, patch_size=32, stride=16):
    H, W, C = features.shape
    X, Y = [], []
    for i in range(0, H - patch_size, stride):
        for j in range(0, W - patch_size, stride):
            xp = features[i:i+patch_size, j:j+patch_size, :]
            yp = labels[i:i+patch_size, j:j+patch_size]
            X.append(xp.transpose(2,0,1))
            Y.append(yp)
    return np.array(X, dtype=np.float32), np.array(Y, dtype=np.int64)

