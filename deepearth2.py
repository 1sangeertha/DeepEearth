    labels[(nbr_diff < -0.3) & (ndvi_diff < -0.1)] = 9
    labels[(ndwi_diff < -0.15) & (arrays['NDWI_19'] > 0.1)] = 8
    labels[(mndwi_diff < -0.2) & (arrays['MNDWI_19'] > 0.0) & (arrays['NDBI_24'] > 0.05)] = 7
    labels[(ndbi_diff > 0.15) & (arrays['NDVI_24'] < 0.1) & (arrays['NDVI_19'] > 0.05)] = 6
