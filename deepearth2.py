def get_dynamic_world_labels(region_geom, year):
    """
    Get real land cover labels from Google Dynamic World
    for a specific year
    """
    dw = (ee.ImageCollection('GOOGLE/DYNAMICWORLD/V1')
          .filterBounds(region_geom)
          .filterDate(f'{year}-01-01', f'{year}-12-31')
          .mode()  # most common class per pixel
          .reproject(crs='EPSG:4326', scale=SCALE))

    label = dw.select('label')

    try:
        d   = label.sampleRectangle(region=region_geom,
                                     defaultValue=0).getInfo()
        arr = np.array(d['properties']['label'], dtype=np.int64)
        return arr
    except Exception as e:
        print(f"   ⚠️ DW failed: {e}")
        return None

def dw_to_deepearth_labels(dw_2019, dw_2024, arr19, arr24):
    """
    Convert Dynamic World classes → DeepEarth 11 classes

    Dynamic World classes:
    0=water, 1=trees, 2=grass, 3=flooded_veg,
    4=crops, 5=shrub, 6=built, 7=bare, 8=snow_ice

    DeepEarth classes:
    0=No Change, 1=Temp Veg Loss, 2=Perm Deforestation,
    3=Forest Degradation, 4=Urban Expansion, 5=Industrial,
    6=Mining, 7=Sand Mining, 8=Water Shrinkage,
    9=Burn Scars, 10=Agri Expansion
    """
    H = min(dw_2019.shape[0], dw_2024.shape[0])
    W = min(dw_2019.shape[1], dw_2024.shape[1])
    dw19 = dw_2019[:H, :W]
    dw24 = dw_2024[:H, :W]

    # Start with spectral pseudo-labels as base
    arr19_c = arr19[:H, :W, :]
    arr24_c = arr24[:H, :W, :]
    labels  = make_labels(arr19_c, arr24_c)

    # ── Override with real DW labels ──

    # Was trees in 2019, now built in 2024 → Urban Expansion
    labels[(dw19 == 1) & (dw24 == 6)] = 4

    # Was trees in 2019, now bare/grass in 2024 → Deforestation
    labels[(dw19 == 1) & (dw24 == 7)] = 2
    labels[(dw19 == 1) & (dw24 == 2)] = 2

    # Was trees/shrub in 2019, degraded in 2024 → Forest Degradation
    labels[(dw19 == 1) & (dw24 == 5)] = 3
    labels[(dw19 == 5) & (dw24 == 7)] = 3

    # Was water in 2019, now bare in 2024 → Water Body Shrinkage
    labels[(dw19 == 0) & (dw24 == 7)] = 8
    labels[(dw19 == 0) & (dw24 == 2)] = 8

    # Was bare/shrub in 2019, now built in 2024 → Industrial/Mining
    labels[(dw19 == 7) & (dw24 == 6)] = 5
    labels[(dw19 == 5) & (dw24 == 6)] = 6

    # Was anything, now crops in 2024 → Agricultural Expansion
    labels[(dw19 != 4) & (dw24 == 4)] = 10

    # Was flooded veg in 2019, now less → Temp Veg Loss
    labels[(dw19 == 3) & (dw24 != 3)] = 1

    # Both years same → No Change
    labels[(dw19 == dw24)] = 0

    return labels

